const isChinese = (lang) => lang?.toLowerCase().startsWith("zh");

export function translateText(value, language) {
  if (value === undefined || value === null || isChinese(language)) return value;
  return EN_TEXT[value] ?? value;
}

export function useDkTranslate() {
  const { i18n } = useTranslation();
  return (value) => translateText(value, i18n.language);
}
